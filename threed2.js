
function setup() { 
      
    // Create Canvas of size 600*600 
    createCanvas(700, 700, WEBGL); 
} 
   
function draw() { 
      
    // Set background color 
    background(200); 
     
    // Set filled color of sphere 
    fill('green'); 
     
    // sphere() function called 
    sphere(100); 
} 
